import { useStore } from './store'

export type Language = 'zh' | 'en'

/**
 * Pick localized data field
 * zh mode: zh → fallback (original title, skip en)
 * en mode: en → fallback (original title, skip zh)
 * fallback is usually the original title from the API (often English)
 */
export function pickField(
  lang: Language,
  zh: string | null | undefined,
  en: string | null | undefined,
  fallback: string
): string {
  if (lang === 'en') return en ?? fallback
  return zh ?? fallback
}

/**
 * Pick localized array field
 * en mode: prefer en, fallback to fallback (empty)
 * zh mode: prefer zh, fallback to fallback (empty)
 */
export function pickArrayField(
  lang: Language,
  zh: string[] | null | undefined,
  en: string[] | null | undefined
): string[] {
  if (lang === 'en') return (en && en.length > 0) ? en : []
  return (zh && zh.length > 0) ? zh : []
}

/** React hook — returns current language + translations */
export function useLocale() {
  const lang = (useStore((s) => s.settings.language) ?? 'zh') as Language
  return { t: translations[lang], lang }
}

