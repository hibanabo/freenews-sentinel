interface StoreSchema {
  storeVersion: number
  settings: Settings
  // Legacy payload keys (pre-SQLite) kept optional for one-time cleanup.
  keywords?: Keyword[]
  alerts?: Alert[]
  briefs?: Brief[]
  topicRuns?: TopicRunRecord[]
  briefRuntime?: {
    lastAutoBriefAt: string | null
  }
}

