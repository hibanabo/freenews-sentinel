  /** Per-topic importance threshold override; null = use global Settings.importanceThreshold */
  topicImportanceThreshold: number | null
  /** Per-topic negative sentiment threshold override; null = use global Settings.negativeSentimentThreshold */
  topicNegativeSentimentThreshold: number | null
