// ─── Data migration: maintain settings version number only ───
// ─── 数据迁移：仅维护 settings 版本号 ─────────────────────────
;(function migrateStore() {
  const savedVersion = (store.get('storeVersion') as number) ?? 0
  const hasLegacyPayload =
    store.has('keywords') ||
    store.has('alerts') ||
    store.has('briefs') ||
    store.has('topicRuns') ||
    store.has('briefRuntime')

  if (hasLegacyPayload) {
    store.delete('keywords')
    store.delete('alerts')
    store.delete('briefs')
    store.delete('topicRuns')
    store.delete('briefRuntime')
  }

  if (savedVersion >= CURRENT_STORE_VERSION && !hasLegacyPayload) return

  store.set('storeVersion', CURRENT_STORE_VERSION)
  if (process.env.NODE_ENV !== 'production') {
    console.log(`[Store] Settings schema version set to v${CURRENT_STORE_VERSION}`)
  }
})()
